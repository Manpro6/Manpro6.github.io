          <h2 class="featurette-heading">Misi PPLK<span class="text-muted"></span></h2>
          <hr>
          <p class="lead">PPLK (Pusat Pelatihan dan Layanan Komputer) yang berfungsi Melayani dan mengelola kebutuhan penggunaan komputer, Fakultas/Lembaga, Unit, Jurusan, maupun Dosen Mata Kuliah, dalam rangka mendukung proses belajar mengajar. <br><br>

		  Mengkoordinasikan jadwal penggunaan komputer baik praktikum, bebas/Internet, maupun untuk tugas-tugas lain dengan pihak-pihak terkait. <br><br>

		  Merencanakan pengembangan fasilitas (kuantitas dan kualitas) laboratorium, pemeliharaannya dalam mengikuti perkembangan kemajuan teknologi. <br><br>

		  Ikut serta mengembangkan ilmu pengetahuan dan ketrampilan di bidang ilmu komputer dan ilmu-ilmu pendukungnya sesuai dengan kebutuhan fakultas-fakultas.</p>
