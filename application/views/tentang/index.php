  		<h2 class="featurette-heading">Tentang PPLK</h2>
  		<hr>
  		Pusat Pelatihan dan Layanan Komputer Universitas Kristen Duta Wacana (PPLK UKDW) merupakan penggabungan Unit Pelaksana Teknik (UPT) Komputer dengan Laboratorium Komputer. Unit ini merupakan penataan ulang UPT Komputer dan Laboratorium Komputer yang semula berada di bawah binaan jurusan Teknik Informatika UKDW. (SK Rektor UKDW no.014/B.07.b/UKDW/1996). <br><br>

		PPLK bertanggung jawab kepada rektor. Secara umum PPLK mempunyai dua tugas yaitu menangani komputer yang berada di laboratorium dan menangani pemeliharaan komputer di unit-unit, pelayanan pelatihan komputer, sertifikasi Telematika, sertifikasi Internasional (Pearson Vue). Komputer yang berada di laboratorium umumnya digunakan untuk praktikum oleh jurusan yang ada di UKDW; selain itu juga digunakan untuk pelatihan-pelatihan yang dilakukan unit-unit di UKDW termasuk unit kemahasiswaan.
